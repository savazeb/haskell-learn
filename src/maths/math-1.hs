--MATH - BUILT-IN FUNCTIONS

main = do
 print(sqrt 81) -- returns square root of a number
 print(round 5.6) -- rounds to the nearest ones
 print(round 5.4) -- rounds to the nearest ones
 print(floor 5.679) -- rounds down to the nearest ones
 print(ceiling 5.2) -- rounds up to the nearest ones
 print(mod 5 4) -- can also be written as 5 'mod' 4
 print(rem 5 4) -- can also be written as 5 'rem' 4
