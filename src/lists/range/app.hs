-- List

x = [0..5]

main = do
 print(null x) -- checks if the list is empty 
 print(reverse x) -- list in Reverse Order
 print(length x) -- lenght of list
 print(maximum x) -- max
 print(minimum x) -- min
 print(sum x) -- sum
 print(product x) -- multiplication of list
 print(elem 6 x) -- checks if list contains x
